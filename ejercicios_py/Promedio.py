# Calcula el promedio estudiantil de 5 materias
nota2 = int(input("Ingrese la nota de Lenguaje"))
nota1 = int(input("Ingrese la nota de Matematicas"))
nota3 = int(input("Ingrese la nota de Biologia"))
nota4 = int(input("Ingrese la nota de Fisica"))
nota5 = int(input("Ingrese la nota de Computacion"))

